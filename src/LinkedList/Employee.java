package LinkedList;

public class Employee {

	public String name ;
	public String age ;
	
	/* Parameterised Constructor*/
	public Employee(String name, String age) {
		super();
		this.name = name;
		this.age = age;
	}
	/* Setters and Getters */ 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
	
	
	
}
